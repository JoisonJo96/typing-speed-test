function startTimer() {
  timeLeft = 15;
  updateTimer();
  timerInterval = setInterval(() => {
    timeLeft--;
    updateTimer();
    if (timeLeft <= 0) {
      clearInterval(timerInterval);
      showCorrectAnswer();
    }
  }, 1000);
}

function updateTimer() {
  timerEl.textContent = timeLeft;
  if (timeLeft <= 5) {
    timerEl.classList.add("flash-red");
  } else {
    timerEl.classList.remove("flash-red");
  }
}

